create database sales_db;

create table customers(Customer_ID int auto_increment primary key, Customer_Name varchar(25) not null, location varchar(23) not null,
gender enum('Male','Female','Others') not null, Age int check(Age >= 0) not null, Email varchar(90) unique not null, JoinDate date not null);

create table products(Product_ID int auto_increment primary key, ProductName varchar(25)not null, Category varchar(25) NOT NULL, Price decimal(10,2) not null,
Stock int not null, index (Category));

create table sales(SaleID int auto_increment primary key, Customer_ID int not null, Product_ID int not null, Quantity int not null,
SalesDate date default(current_date), Revenue decimal(10,2) not null,
foreign key (Customer_ID) references customers(Customer_ID) on update cascade on delete cascade,
foreign key (Product_ID) references Products(Product_ID) on update cascade on delete cascade, index (Customer_ID), index (Product_ID));

